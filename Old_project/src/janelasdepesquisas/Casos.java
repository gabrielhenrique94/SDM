    private void list1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_list1ItemStateChanged
        String selecionado = list1.getSelectedItem();
        String codigo = selecionado.split(" - ")[0];

        CasoDAO casodao = new CasoDAO();
        Caso ocaso = casodao.selectPorCodigo(Integer.parseInt(codigo));
        int codigodoenca = ocaso.getDoenca().getCodigo();
        DoencaDAO doencadao = new DoencaDAO();
        Doenca doe = doencadao.selectPorCodigo(codigodoenca);

        JanelaMostraCaso jcd = new JanelaMostraCaso(ocaso, doe);
        jcd.setName("Caso: " + ocaso.getCodigo());

        JanelaPrincipal.getInstance().jTabbedPaneHome.add(jcd);
        JanelaPrincipal.getInstance().jTabbedPaneHome.setSelectedComponent(jcd);
        JanelaPrincipal.getInstance().jTabbedPaneHome.remove(this);

}//GEN-LAST:event_list1ItemStateChanged

