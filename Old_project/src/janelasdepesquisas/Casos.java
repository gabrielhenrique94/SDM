    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        DoencaDAO doeDAO = new DoencaDAO();
        ArrayList<Doenca> doenca = doeDAO.selectNomeParcial(jTextField1.getText());
        
        ArrayList<Integer> coddoenca = new ArrayList<Integer>();
        ArrayList<Caso> lista = new ArrayList<Caso>();
        ArrayList<Caso> casos = new ArrayList<Caso>();

        for(Doenca d: doenca){
            coddoenca.add(d.getCodigo());
        }
        
        for(int i: coddoenca){

            CasoDAO dao = new CasoDAO();
            casos = dao.selectporDoenca(i);
            lista.addAll(casos);
        }    
        
        atualizaLista(lista);
        
}//GEN-LAST:event_jTextField1KeyReleased

