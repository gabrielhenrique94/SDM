   public void atualizaLista(ArrayList<Caso> casos) {
        list1.removeAll();
        Iterator iterator = casos.iterator();
        while (iterator.hasNext()) {
            Caso caso = (Caso)iterator.next();
            Doenca doenca = caso.getDoenca();
            System.out.println(doenca);
            DoencaDAO doeDAO = new DoencaDAO();
            Doenca doe = doeDAO.selectPorCodigo(doenca.getCodigo());
            String nomedoenca = doe.getNome();

            String dadosCaso = Integer.toString(caso.getCodigo()) + " - " + nomedoenca + " - " + caso.getIdade() + "anos";
            if(!casosNaLista.contains(dadosCaso)){
            list1.add(dadosCaso);
            }

        }

    }

