package ProcessosBD;

import java.sql.Date;

public class NovaDoenca {
    private int codigo, tema, votoFavor, votoContra;
    private String comentario;
    private String sintomas;
    private Date dataPedido;
    private Doenca doenca;

    public NovaDoenca() {
    }

    public NovaDoenca(int codigo, int tema, int votoFavor, int votoContra, String comentario, String sintomas, Doenca doenca) {
        this.codigo = codigo;
        this.tema = tema;
        this.votoFavor = votoFavor;
        this.votoContra = votoContra;
        this.comentario = comentario;
        this.sintomas = sintomas;
        this.doenca = doenca;
    }

    //<editor-fold defaultstate="collapsed" desc="gets/sets">
    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getComentario() {
        return comentario;
    }

    public void setComentario(String comentario) {
        this.comentario = comentario;
    }

    public Date getDataPedido() {
        return dataPedido;
    }

    public void setDataPedido(Date dataPedido) {
        this.dataPedido = dataPedido;
    }

    public Doenca getDoenca() {
        return doenca;
    }

    public void setDoenca(Doenca doenca) {
        this.doenca = doenca;
    }

    public String getSintomas() {
        return sintomas;
    }

    public void setSintomas(String sintomas) {
        this.sintomas = sintomas;
    }

    public int getTema() {
        return tema;
    }

    public void setTema(int tema) {
        this.tema = tema;
    }

    public int getVotoContra() {
        return votoContra;
    }

    public void setVotoContra(int votoContra) {
        this.votoContra = votoContra;
    }

    public int getVotoFavor() {
        return votoFavor;
    }

    public void setVotoFavor(int votoFavor) {
        this.votoFavor = votoFavor;
    }
    //</editor-fold>    
}
