package ProcessosBD;

public class UsuarioNovoSin {
    
    private Usuario usuario;
    private NovoSintoma novoSintoma;
    private int votou;

    public UsuarioNovoSin() {
    }

    public UsuarioNovoSin(Usuario usuario, NovoSintoma novoSintoma, int votou) {
        this.usuario = usuario;
        this.novoSintoma = novoSintoma;
        this.votou = votou;
    }

    public NovoSintoma getNovoSintoma() {
        return novoSintoma;
    }

    public void setNovoSintoma(NovoSintoma novoSintoma) {
        this.novoSintoma = novoSintoma;
    }

    public Usuario getUsuario() {
        return usuario;
    }

    public void setUsuario(Usuario usuario) {
        this.usuario = usuario;
    }

    public int getVotou() {
        return votou;
    }

    public void setVotou(int votou) {
        this.votou = votou;
    }
}
