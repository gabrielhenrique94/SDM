package ProcessosBD;

import java.util.Date;

public class Paciente {
    private int codigo , telResidencial, telCelular;
    private String nome, rg, endereco, endComplemento, cidade, uf, estadoCivil;
    private Date dataNasc;
    private FichaMedica fichaMedica;
    private long cpf;
    
    public Paciente() {
    }

    public Paciente(int codigo, long cpf, int telResidencial, int telCelular, String nome,
            String rg, String endereco, String endComplemento, String cidade, String uf, String estadoCivil,
            Date dataNasc, FichaMedica ficha) {
        this.codigo = codigo;
        this.cpf = cpf;
        this.telResidencial = telResidencial;
        this.telCelular = telCelular;
        this.nome = nome;
        this.rg = rg;
        this.endereco = endereco;
        this.endComplemento = endComplemento;
        this.cidade = cidade;
        this.uf = uf;
        this.estadoCivil = estadoCivil;
        this.dataNasc = dataNasc;
        this.fichaMedica = ficha;
    }

    //<editor-fold defaultstate="collapsed" desc="gets/sets">
    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public long getCpf() {
        return cpf;
    }

    public void setCpf(long cpf) {
        this.cpf = cpf;
    }

    public Date getDataNasc() {
        return dataNasc;
    }

    public void setDataNasc(Date dataNasc) {
        this.dataNasc = dataNasc;
    }

    public String getEndComplemento() {
        return endComplemento;
    }

    public void setEndComplemento(String endComplemento) {
        this.endComplemento = endComplemento;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getEstadoCivil() {
        return estadoCivil;
    }

    public void setEstadoCivil(String estadoCivil) {
        this.estadoCivil = estadoCivil;
    }

    public FichaMedica getFichaMedica() {
        return fichaMedica;
    }

    public void setFichaMedica(FichaMedica fichaMedica) {
        this.fichaMedica = fichaMedica;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }

    public int getTelCelular() {
        return telCelular;
    }

    public void setTelCelular(int telCelular) {
        this.telCelular = telCelular;
    }

    public int getTelResidencial() {
        return telResidencial;
    }

    public void setTelResidencial(int telResidencial) {
        this.telResidencial = telResidencial;
    }

    public String getUf() {
        return uf;
    }

    public void setUf(String uf) {
        this.uf = uf;
    }
    //</editor-fold>
}
